#include <stdio.h>
#include <stdlib.h>

/**
 * main - prints out number of arguements passed to it
 * @argc: counts the number of arguements
 * @argv: prints the arguments in a commandline
 * Return: zero
 */

int __attribute__ ((unused)) main(int argc, char *argv[])
{
	printf("%d\n", argc);

	return (0);
}
